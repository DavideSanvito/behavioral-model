int main(int argc, char *argv[]) {
  pktpipe_mgr_t *pktpipe_mgr;
  pktpipe_init(&pktpipe_mgr, "ipc:///tmp/pktpipe_test.ipc");
  pktpipe_set_packet_receiver(pktpipe_mgr, my_receiver, NULL);
  pktpipe_send(pktpipe_mgr, 1, pkt78, sizeof(pkt78));
  sleep(1000);
  return 0;
}
